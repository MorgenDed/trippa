// Combine 'day-trips' and 'museums' for the "Cheap Day Trips & Tickets" category
const dayTrips = categoryDeals['day-trips'] || [];
const museums = categoryDeals['museums'] || [];
const combinedDayTrips = [...dayTrips, ...museums].sort(() => 0.5 - Math.random());
categoryDeals['day-trips'] = combinedDayTrips;

// Fill 'all-deals' with a mix of deals from other categories
const allDeals = Object.values(categoryDeals).flat();
// Shuffle array to get random mix
const shuffled = allDeals.sort(() => 0.5 - Math.random());
// Take about 70% of deals
categoryDeals['all-deals'] = shuffled.slice(0, Math.ceil(allDeals.length * 0.7));

export const getDealById = (id: string | number) => {
  const numericId = Number(id);
  for (const category in categoryDeals) {
    const deal = categoryDeals[category].find((d) => d.id === numericId);
    if (deal) return deal;
  }
  return null;
};
