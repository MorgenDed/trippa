let checked = 0;
let broken = 0;

urls.forEach(url => {
  https.get(url, (res) => {
    checked++;
    if (res.statusCode !== 200) {
      console.log(`BROKEN (${res.statusCode}): ${url}`);
      broken++;
    }
    if (checked === urls.length) {
      console.log(`\nChecked ${checked} images. Found ${broken} broken links.`);
    }
  }).on('error', (e) => {
    checked++;
    console.error(`ERROR: ${url} - ${e.message}`);
    broken++;
    if (checked === urls.length) {
      console.log(`\nChecked ${checked} images. Found ${broken} broken links.`);
    }
  });
});
